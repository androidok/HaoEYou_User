package com.haoeyou.user.bean;

import java.util.List;

/**
 * 类名: {@link categoryList}
 * <br/> 功能描述: 科室关注二级基类
 * <br/> 作者: MouTao
 * <br/> 时间: 2017/6/5
 */
public class categoryList {
    /**
     * 科室名称
     */
    private String name;
    /**
     * 子科室
     */
    private List<childNames> childNames;

    public categoryList() {
    }

    public categoryList(String name, List<childNames> childNames) {
        this.name = name;
        this.childNames = childNames;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<childNames> getChildNames() {
        return childNames;
    }

    public void setChildNames(List<childNames> childNames) {
        this.childNames = childNames;
    }
}
