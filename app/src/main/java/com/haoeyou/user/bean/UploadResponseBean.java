public class UploadResponseBean {
    private String errorCode;
    private String errorMsg;
    private String fileID;
    private String fileIDmedical_history_id;
    private String patient_id;

    public UploadResponseBean() {
    }

    public UploadResponseBean(String errorCode, String errorMsg, String fileID, String fileIDmedical_history_id, 
                              String patient_id) {
        this.errorCode = errorCode;
        this.errorMsg = errorMsg;
        this.fileID = fileID;
        this.fileIDmedical_history_id = fileIDmedical_history_id;
        this.patient_id = patient_id;
    }

    public String getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(String errorCode) {
        this.errorCode = errorCode;
    }

    public String getErrorMsg() {
        return errorMsg;
    }

    public void setErrorMsg(String errorMsg) {
        this.errorMsg = errorMsg;
    }

    public String getFileID() {
        return fileID;
    }

    public void setFileID(String fileID) {
        this.fileID = fileID;
    }

    public String getFileIDmedical_history_id() {
        return fileIDmedical_history_id;
    }

    public void setFileIDmedical_history_id(String fileIDmedical_history_id) {
        this.fileIDmedical_history_id = fileIDmedical_history_id;
    }

    public String getPatient_id() {
        return patient_id;
    }

    public void setPatient_id(String patient_id) {
        this.patient_id = patient_id;
    }
}
