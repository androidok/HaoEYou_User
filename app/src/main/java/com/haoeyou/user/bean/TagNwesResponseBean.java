package com.haoeyou.user.bean;

import java.util.ArrayList;

/**
 * Created by Mou on 2017/7/11.
 */

public class TagNwesResponseBean {
    private String errorCode;
    private String errorMsg;
    private String total;
    private ArrayList<Artical> articles;

    public TagNwesResponseBean() {
    }

    public TagNwesResponseBean(String errorCode, String errorMsg, String total, ArrayList<Artical> articles) {
        this.errorCode = errorCode;
        this.errorMsg = errorMsg;
        this.total = total;
        this.articles = articles;
    }

    public String getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(String errorCode) {
        this.errorCode = errorCode;
    }

    public String getErrorMsg() {
        return errorMsg;
    }

    public void setErrorMsg(String errorMsg) {
        this.errorMsg = errorMsg;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public ArrayList<Artical> getArticles() {
        return articles;
    }

    public void setArticles(ArrayList<Artical> articles) {
        this.articles = articles;
    }
}
