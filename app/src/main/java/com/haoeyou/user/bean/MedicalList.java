package com.haoeyou.user.bean;

import java.io.Serializable;
import java.util.ArrayList;

/**
 * Created by Mou on 2017/7/7.
 */

public class MedicalList implements Serializable {
    private String id;
    private String title;
    private String upload_time;
    private ArrayList<String> file_id_list;

    public MedicalList() {
    }

    public MedicalList(String id, String title, String upload_time, ArrayList<String> file_id_list) {
        this.id = id;
        this.title = title;
        this.upload_time = upload_time;
        this.file_id_list = file_id_list;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUpload_time() {
        return upload_time;
    }

    public void setUpload_time(String upload_time) {
        this.upload_time = upload_time;
    }

    public ArrayList<String> getFile_id_list() {
        return file_id_list;
    }

    public void setFile_id_list(ArrayList<String> file_id_list) {
        this.file_id_list = file_id_list;
    }
}
